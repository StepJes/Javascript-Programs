//perimeter of circle
function circle(r){
    const pi=3.14;
return (2*pi*r);
}
exports.circle=circle;

//perimeter of square
function square(a){
    return (4*a);
    }
    exports.square=square;


//perimeter of rectangle

function rectangle(a,b){
    return (2*(a+b));
     }
    exports.rectangle=rectangle;
        
        